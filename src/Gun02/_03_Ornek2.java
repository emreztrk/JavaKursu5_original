package Gun02;

public class _03_Ornek2 {
    public static void main(String[] args) {

        System.out.println("Merhaba Dünya");    // çift tırnak arasındaki her şeyi ekrana yazdırırım.
        System.out.println("merhaba dünya 2");  // (ln) var ise yazdıktan sonra alt satıra geçer
        System.out.print("merhaba dünya 3");
        System.out.println();                   // bir şey yazmaz sadece yeni satır açar
        System.out.print("merhaba dünya 4");
        System.out.println("Merhaba \n Dünya"); // \n --> yeni satır açar synı ln gini

    }

}
